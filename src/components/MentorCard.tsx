import { Star, Phone, Users, MessageCircle, Heart } from "lucide-react";
import { Badge } from "@/components/ui/badge";
import { Button } from "@/components/ui/button";
import { Link } from "react-router-dom";
import { Card, CardContent } from "@/components/ui/card";
import { Avatar, AvatarImage, AvatarFallback } from "@/components/ui/avatar";
import { useState, useEffect } from "react";

// Update the MentorProfile type to include connectionOptions
export interface MentorProfile {
  id: string;
  name: string;
  title: string;
  image: string;
  categories: string[];
  rating: number;
  reviewCount: number;
  price: number;
  bio: string;
  connectionOptions: string[];
  username?: string; // Optional username for new profile route
  expertise_tags?: string[];
  tagline?: string; // Generated tagline like "Senior @ IIT Delhi | Computer Science"
}

interface MentorCardProps {
  mentor: MentorProfile;
  isNew?: boolean;
  isFavorite?: boolean;
  onToggleFavorite?: (mentorId: string) => void;
}

const MentorCard = ({
  mentor,
  isNew,
  isFavorite = false,
  onToggleFavorite,
}: MentorCardProps) => {
  const nameParts = mentor.name.split(" ");
  const initials =
    nameParts.length > 1
      ? `${nameParts[0][0]}${nameParts[nameParts.length - 1][0]}`
      : mentor.name[0];

  const handleFavoriteClick = (e: React.MouseEvent) => {
    e.preventDefault();
    e.stopPropagation();
    if (onToggleFavorite) {
      onToggleFavorite(mentor.id);
    }
  };

  const getConnectionIcon = (option: string) => {
    const lowerOption = option.toLowerCase();
    if (lowerOption.includes("call") || lowerOption.includes("1:1")) {
      return <Phone className="h-3.5 w-3.5" />;
    } else if (lowerOption.includes("group")) {
      return <Users className="h-3.5 w-3.5" />;
    } else if (lowerOption.includes("chat") || lowerOption.includes("doubt")) {
      return <MessageCircle className="h-3.5 w-3.5" />;
    }
    return <Phone className="h-3.5 w-3.5" />;
  };

  return (
    <Card className="overflow-visible shadow-md hover:shadow-xl transition-all duration-300 h-full w-full max-w-[340px] mx-auto border border-gray-200 bg-white rounded-2xl">
      <CardContent className="p-6 relative">
        {isNew && (
          <div className="absolute -top-2 -right-2 z-50 pointer-events-none">
            <div className="bg-gradient-to-r from-matepeak-primary to-matepeak-secondary text-white text-xs font-bold px-2 py-1 rounded-full shadow-lg">
              RECENT
            </div>
          </div>
        )}

        {/* Favorite Button */}
        {onToggleFavorite && (
          <button
            onClick={handleFavoriteClick}
            className="absolute top-3 right-3 z-20 p-1 rounded-full bg-white/90 hover:bg-white shadow-sm hover:shadow-md transition-all backdrop-blur-sm"
            aria-label={
              isFavorite ? "Remove from favorites" : "Add to favorites"
            }
          >
            <Heart
              className={`h-4 w-4 transition-all ${
                isFavorite
                  ? "fill-red-500 text-red-500"
                  : "text-gray-400 hover:text-red-500"
              }`}
            />
          </button>
        )}

        {/* Header: Avatar, Name, Tagline, Rating */}
        <div className="flex items-start gap-4 mb-4 pr-10">
          <Avatar className="h-16 w-16 flex-shrink-0 border-2 border-gray-100">
            <AvatarImage
              src={mentor.image}
              alt={mentor.name}
              className="object-cover"
            />
            <AvatarFallback className="bg-matepeak-primary text-white font-semibold text-lg">
              {initials}
            </AvatarFallback>
          </Avatar>

          <div className="flex-1 min-w-0">
            <h3 className="font-bold text-gray-900 text-base mb-0.5 line-clamp-1 pr-1">
              {mentor.name}
            </h3>

            {/* Tagline */}
            {mentor.tagline && (
              <p className="text-gray-600 text-xs mb-1.5 line-clamp-1">
                {mentor.tagline}
              </p>
            )}

            {/* Rating or No Reviews Badge */}
            {mentor.rating > 0 && mentor.reviewCount > 0 ? (
              <div className="flex items-center gap-1.5">
                <Star className="h-4 w-4 fill-yellow-400 text-yellow-400 flex-shrink-0" />
                <span className="text-sm font-bold text-gray-900">
                  {mentor.rating.toFixed(1)}
                </span>
                <span className="text-xs text-gray-500">
                  ({mentor.reviewCount})
                </span>
              </div>
            ) : (
              <span className="text-xs text-gray-500 italic">
                No reviews yet
              </span>
            )}
          </div>
        </div>

        {/* Bio */}
        {mentor.bio && (
          <p className="text-sm text-gray-600 leading-relaxed mb-4 line-clamp-2">
            {mentor.bio}
          </p>
        )}

        {/* Expertise Section */}
        <div className="mb-4">
          <h4 className="text-xs font-bold text-gray-900 uppercase tracking-wide mb-2.5">
            EXPERTISE
          </h4>
          <div className="flex flex-wrap gap-2">
            {mentor.categories.slice(0, 3).map((category, index) => (
              <Badge
                key={index}
                variant="outline"
                className="bg-white text-gray-700 border-gray-300 text-xs font-medium px-3 py-1 rounded-md hover:bg-gray-50 transition-colors"
              >
                {category}
              </Badge>
            ))}
          </div>
        </div>

        {/* Available Services */}
        <div className="mb-6">
          <h4 className="text-xs font-bold text-gray-900 uppercase tracking-wide mb-2.5">
            AVAILABLE SERVICES
          </h4>
          <div className="flex flex-wrap gap-2">
            {mentor.connectionOptions.slice(0, 3).map((option, index) => (
              <Badge
                key={index}
                variant="outline"
                className="bg-white text-gray-700 border-gray-300 text-xs font-medium px-3 py-1 rounded-md hover:bg-gray-50 transition-colors"
              >
                <span className="flex items-center gap-1.5">
                  {getConnectionIcon(option)}
                  {option}
                </span>
              </Badge>
            ))}
          </div>
        </div>

        {/* Footer: Price and CTA */}
        <div className="flex items-center justify-between pt-4 border-t border-gray-200">
          <div>
            <p className="text-xs text-gray-500 mb-0.5">Starting from</p>
            <p className="text-xl font-bold text-gray-900">
              â‚¹{mentor.price}
              <span className="text-xs text-gray-500 font-normal ml-1">
                /session
              </span>
            </p>
          </div>
          <Link
            to={
              mentor.username
                ? `/mentor/${mentor.username}`
                : `/mentors/${mentor.id}`
            }
          >
            <Button className="bg-gray-900 hover:bg-gray-800 text-white text-sm font-semibold px-6 py-2 rounded-lg transition-colors">
              View Profile
            </Button>
          </Link>
        </div>
      </CardContent>
    </Card>
  );
};

export default MentorCard;
